#!/bin/bash
# راه‌اندازی ربات هوشمند تحلیل ارزهای دیجیتال

# رنگ‌های ANSI
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
RED='\033[0;31m'
NC='\033[0m' # No Color

# عنوان
echo -e "${YELLOW}=== راه‌اندازی ربات هوشمند تحلیل ارزهای دیجیتال ===${NC}"
echo

# منوی انتخاب
echo -e "${BLUE}لطفاً یکی از گزینه‌های زیر را انتخاب کنید:${NC}"
echo "1) اجرای نسخه ساده (بدون نیاز به کلید API هوش مصنوعی)"
echo "2) اجرای نسخه هوشمند (با استفاده از هوش مصنوعی)"
echo "3) راه‌اندازی زمانبندی خودکار (ارسال گزارش‌ها در زمان‌های مشخص)"
echo "4) توقف زمانبندی خودکار"
echo "5) نمایش راهنمای استفاده"
echo "0) خروج"
echo

# دریافت انتخاب کاربر
read -p "انتخاب شما (0-5): " choice
echo

# اجرای گزینه انتخاب شده
case $choice in
    1)
        echo -e "${GREEN}در حال اجرای نسخه ساده...${NC}"
        ./run_simple_ai.sh
        ;;
    2)
        echo -e "${GREEN}در حال اجرای نسخه هوشمند...${NC}"
        ./demo_ai_features.sh
        ;;
    3)
        echo -e "${GREEN}در حال راه‌اندازی زمانبندی خودکار...${NC}"
        ./start_smart_robot.sh
        ;;
    4)
        echo -e "${GREEN}در حال توقف زمانبندی خودکار...${NC}"
        ./stop_smart_robot.sh
        ;;
    5)
        echo -e "${GREEN}در حال نمایش راهنمای استفاده...${NC}"
        cat راهنمای_استفاده_از_ربات_هوشمند.md
        ;;
    0)
        echo "خروج از برنامه"
        exit 0
        ;;
    *)
        echo -e "${RED}انتخاب نامعتبر!${NC}"
        exit 1
        ;;
esac

echo
echo -e "${BLUE}برای اطلاعات بیشتر، راهنمای استفاده را مطالعه کنید: راهنمای_استفاده_از_ربات_هوشمند.md${NC}"
