#!/bin/bash

# اسکریپت توقف تمام سرویس‌های گزارش‌دهی تلگرام
# این اسکریپت تمام سرویس‌های ارسال گزارش به تلگرام را متوقف می‌کند

echo "🛑 شروع توقف تمام سرویس‌های گزارش‌دهی تلگرام..."

# توقف گزارش‌های سه لایه‌ای هر ۱۰ دقیقه
echo "۱. توقف سرویس گزارش‌های سه لایه‌ای..."
bash ./stop_ten_minute_reporter.sh
sleep 2

# توقف سرویس ربات هوشمند
echo "۲. توقف سرویس گزارش‌های هوشمند..."
bash ./stop_smart_robot.sh
sleep 2

# توقف سرویس گزارش‌های ساده
echo "۳. توقف سرویس گزارش‌های ساده..."
bash ./stop_scheduler.sh
sleep 2

echo "✅ تمام سرویس‌های گزارش‌دهی با موفقیت متوقف شدند."
