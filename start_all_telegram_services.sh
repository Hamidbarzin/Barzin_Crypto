#!/bin/bash

# اسکریپت راه‌اندازی تمام سرویس‌های گزارش‌دهی تلگرام
# این اسکریپت تمام سرویس‌های ارسال گزارش به تلگرام را راه‌اندازی می‌کند

echo "🤖 شروع راه‌اندازی تمام سرویس‌های گزارش‌دهی تلگرام..."

# راه‌اندازی گزارش‌های سه لایه‌ای هر ۱۰ دقیقه
echo "۱. راه‌اندازی سرویس گزارش‌های سه لایه‌ای (هر ۱۰ دقیقه)..."
bash ./start_ten_minute_reporter.sh
sleep 2

# راه‌اندازی سرویس ربات هوشمند
echo "۲. راه‌اندازی سرویس گزارش‌های هوشمند..."
bash ./start_smart_robot.sh
sleep 2

# راه‌اندازی سرویس گزارش‌های ساده
echo "۳. راه‌اندازی سرویس گزارش‌های ساده..."
bash ./simple_scheduler.sh
sleep 2

# ارسال یک پیام تست برای تایید عملکرد سیستم
echo "۴. ارسال پیام تست تلگرام برای تایید..."
curl -s 'http://localhost:5000/api/test-telegram'

echo "✅ تمام سرویس‌های گزارش‌دهی با موفقیت راه‌اندازی شدند."
echo "از این پس شما گزارش‌های متنوعی را در تلگرام دریافت خواهید کرد."
